    private void panel_igualMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panel_igualMousePressed
        // TODO add your handling code hereca
        double numero2;
        if (operacion.equalsIgnoreCase("Nula")) {
            lbl_pantalla.setText(cadena_Numero);
        } else if (operacion.equalsIgnoreCase("Sumar")) {
            numero2 = Double.parseDouble(cadena_Numero);
            resultado = numero + numero2;
            lbl_numero.setText(String.format("%.2f", resultado));
            cadena_Numero = String.valueOf(resultado);
            operacion = "Nula";
            //cadena_Numero = "";
        } else if (operacion.equalsIgnoreCase("Restar")) {
            numero2 = Double.parseDouble(cadena_Numero);
            resultado = numero - numero2;
            lbl_numero.setText(String.format("%.2f", resultado));
            cadena_Numero = String.valueOf(resultado);
            operacion = "Nula";
        } else if (operacion.equalsIgnoreCase("Multi")) {
            numero2 = Double.parseDouble(cadena_Numero);
            resultado = numero * numero2;
            lbl_numero.setText(String.format("%.2f", resultado));
            cadena_Numero = String.valueOf(resultado);
            operacion = "Nula";
        } else if (operacion.equalsIgnoreCase("Div")) {
            numero2 = Double.parseDouble(cadena_Numero);
            resultado = numero / numero2;
            lbl_numero.setText(String.format("%.2f", resultado));
            cadena_Numero = String.valueOf(resultado);
            operacion = "Nula";
        }
        lbl_pantalla.setText(" ");
        //cadena_Numero = "";
        activado = true;


    }//GEN-LAST:event_panel_igualMousePressed

