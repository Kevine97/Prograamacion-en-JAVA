    private void panel_masmenosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panel_masmenosMousePressed
        // TODO add your handling code here:
        int tamano = cadena_Numero.length();

        if (tamano > 0) {
            if (cadena_Numero.charAt(0) != '-') {

                cadena_Numero = "-" + cadena_Numero;

            } else {
                cadena_Numero = cadena_Numero.substring(1, cadena_Numero.length());
            }
            lbl_numero.setText(cadena_Numero);
        }
    }//GEN-LAST:event_panel_masmenosMousePressed

