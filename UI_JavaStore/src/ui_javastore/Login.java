    private void jPanel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MousePressed

       try {
            if (txtusuario.getText().equals("") || txtpass.getText().equals("")) {
                Util.messageWarning("Advertencia", "No puede dejar campos vacios.");
            } else {
                Users user = loginService.getUsersByUsername(txtusuario.getText());
                if (user == null) {
                    Util.messageError("Advertencia", "Usuario no existe.");

                } else {
                    if (user.getPassword().equals(txtpass.getText())) {
                        Util.messageInfo("Bienvenido", "Ha iniciado sesion");
                    } else {
                        Util.messageError("Advertencia", "ContraseÃ±a Incorrecta.");
                    }
                }
            }

        } catch (Exception e) {
            Util.messageError("Advertencia", e.getMessage());
        }

    }//GEN-LAST:event_jPanel2MousePressed

